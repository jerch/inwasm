import * as assert from 'assert';
import { convert16BitTo8BitData } from '.';


describe('convert16BitTo8BitData', () => {
  it('basic test 32 entries', () => {
    const result = convert16BitTo8BitData(new Uint16Array([
      0x1122, 0x3344, 0x5566, 0x7788,
      0x1122, 0x3344, 0x5566, 0x7788,
      0x1122, 0x3344, 0x5566, 0x7788,
      0x1122, 0x3344, 0x5566, 0x7788
    ]));
    assert.deepEqual(
      result,
        new Uint8Array([
        0x11, 0x33, 0x55, 0x77,
        0x11, 0x33, 0x55, 0x77,
        0x11, 0x33, 0x55, 0x77,
        0x11, 0x33, 0x55, 0x77
      ])
    );
  });
});
